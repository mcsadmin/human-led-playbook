DEFAULT_ESTIMATE_SCALE = "tshirt"

# ---------------------------------------------------------------------------
# SkillConfig — generated by preflight.py, consumed by parse_spreadsheet.py
# ---------------------------------------------------------------------------

@dataclass
class LinearMember:
    id: str
    name: str
    email: str
    display_name: str

@dataclass
class LinearLabel:
    id: str
    name: str
    group: str   # parent label group name

@dataclass
class LinearCycle:
    id: str
    name: str
    starts_at: str
    ends_at: str

@dataclass
class LinearTeam:
    id: str
    name: str
    key: str

@dataclass
class LinearStatus:
    id: str
    name: str
    type: str   # backlog | unstarted | started | completed | cancelled

@dataclass
class SkillConfig:
    """
    Produced by Phase 0 (preflight.py) and consumed by Phase 1 (parse_spreadsheet.py).
    Represents the resolved state of the Linear workspace at preflight time.
    """
    team: LinearTeam
    members: list[LinearMember] = field(default_factory=list)
    labels: list[LinearLabel] = field(default_factory=list)
    cycles: list[LinearCycle] = field(default_factory=list)
    statuses: list[LinearStatus] = field(default_factory=list)
    estimate_scale: str = DEFAULT_ESTIMATE_SCALE
    initiative_name: str = ""

    def save(self, path: str) -> None:
        """Serialise to JSON for use across phases."""
        with open(path, "w") as f:
            json.dump({
                "team": self.team.__dict__,
                "members": [m.__dict__ for m in self.members],
                "labels": [l.__dict__ for l in self.labels],
                "cycles": [c.__dict__ for c in self.cycles],
                "statuses": [s.__dict__ for s in self.statuses],
                "estimate_scale": self.estimate_scale,
                "initiative_name": self.initiative_name,
            }, f, indent=2)

    @classmethod
    def load(cls, path: str) -> "SkillConfig":
        """Deserialise from JSON."""
        with open(path) as f:
            data = json.load(f)
        return cls(
            team=LinearTeam(**data["team"]),
            members=[LinearMember(**m) for m in data["members"]],
            labels=[LinearLabel(**l) for l in data["labels"]],
            cycles=[LinearCycle(**c) for c in data["cycles"]],
            statuses=[LinearStatus(**s) for s in data["statuses"]],
            estimate_scale=data.get("estimate_scale", DEFAULT_ESTIMATE_SCALE),
            initiative_name=data.get("initiative_name", ""),
        )

    def find_member(self, name_or_email: str) -> Optional[LinearMember]:
        """Case-insensitive lookup by name, display name, or email."""
        if not name_or_email:
            return None
        needle = name_or_email.strip().lower()
        for m in self.members:
            if (m.name.lower() == needle or
                m.email.lower() == needle or
                m.display_name.lower() == needle):
                return m
        return None

    def find_label(self, name: str, group: Optional[str] = None) -> Optional[LinearLabel]:
        """Lookup a label by name, optionally filtered by group."""
        needle = name.strip().lower()
        for l in self.labels:
            if l.name.lower() == needle:
                if group is None or l.group.lower() == group.lower():
                    return l
        return None

    def find_cycle(self, name: str) -> Optional[LinearCycle]:
        """Case-insensitive lookup by cycle name."""
        needle = name.strip().lower()
        for c in self.cycles:
            if c.name.lower() == needle:
                return c
        return None

    def find_status(self, name: str) -> Optional[LinearStatus]:
        """Case-insensitive lookup by status name."""
        needle = name.strip().lower()
        for s in self.statuses:
            if s.name.lower() == needle:
                return s
        return None

