# ---------------------------------------------------------------------------

@dataclass
class TaskRecord:
    """
    One row from the task register spreadsheet, fully parsed and resolved
    against the Linear workspace state.
    """
    # Raw spreadsheet fields
    task_id: str
    subproject: str
    task_name: str
    # moscow_raw removed from schema
    owner_raw: str
    accountable_raw: str
    consulted_raw: str
    informed_raw: str
    dependencies_raw: str
    estimated_hours_raw: str
    sprint_raw: str
    end_date_raw: str
    status_raw: str
    context_why: str
    task_breakdown: str
    acceptance_criteria: str

    # Resolved / derived fields (populated by parse_spreadsheet.py)
    # moscow removed from schema
    owner_id: Optional[str] = None
    owner_resolved: Optional[str] = None
    dependency_ids: list[str] = field(default_factory=list)   # Task IDs (e.g. ["T01"])
    cycle_id: Optional[str] = None
    cycle_resolved: Optional[str] = None
    status_name: str = "Backlog"
    status_id: Optional[str] = None
    estimate_points: Optional[int] = None
    estimate_label: Optional[str] = None

    # Validation flags
    warnings: list[str] = field(default_factory=list)

    @property
    def issue_title(self) -> str:
        return f"[{self.task_id}] {self.task_name}"

    @property
    def has_warnings(self) -> bool:
        return len(self.warnings) > 0
