"""
config_schema.py
================
Shared data model and configuration schema for the spreadsheet-to-linear skill.

This module defines:
  - The expected spreadsheet column layout (SPREADSHEET_COLUMNS)
  - The SkillConfig dataclass (generated during Phase 0 preflight)
  - The TaskRecord dataclass (produced by parse_spreadsheet.py)
  - The MoSCoW label group name and valid values
  - Default estimate scale name

All other scripts import from this module to ensure a single source of truth.
"""

from __future__ import annotations
from dataclasses import dataclass, field
from typing import Optional
import json
import os

